void setup()
{
  Serial.begin(115200);

  pinMode(TEMT6000, INPUT);

  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
  {
    Serial.println(F("SSD1306 allocation failed"));
    for (;;)
      ;
  }

  display.display();

  display.clearDisplay();
  display.setTextSize(1);
}
int number = 0;

void loop()
{
  display.clearDisplay();
  display.drawBitmap(0, 0, img, 128, 32, 1);
  display.setTextColor(BLACK);
  float volts = analogRead(TEMT6000) * 5 / 1024.0 * 100;
  display.setCursor(32, 0);
  display.println("light: " + String(volts));
  display.display();
}
